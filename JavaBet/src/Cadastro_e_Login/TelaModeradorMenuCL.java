    private void btn_buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_buscarActionPerformed
        try {
            String resposta = JOptionPane.showInputDialog(null, "Digite o ID do usuário que você deseja visualizar");
            if (resposta != null) {
                TelaModeradorBuscarCL novatela = new TelaModeradorBuscarCL();

                novatela.banco = this.banco;
                novatela.volta = this;

                novatela.setID(Integer.parseInt(resposta));
                novatela.mostrarPerfil();
                novatela.setVisible(true);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Usuario não identificado!");
        }

    }//GEN-LAST:event_btn_buscarActionPerformed

