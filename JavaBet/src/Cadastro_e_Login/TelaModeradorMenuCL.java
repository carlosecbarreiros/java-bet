    private void btn_removerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_removerActionPerformed
        String resposta = JOptionPane.showInputDialog(null, "Digite o ID do usuário que você deseja remover");
        if (resposta != null) {
            for (int i = 0; i < banco.bancoDados.length; i++) {
                if (banco.bancoDados[i] != null) {
                    if (banco.bancoDados[i].getID() == Integer.parseInt(resposta)) {
                        banco.bancoDados[i] = null;
                        tableModel = (DefaultTableModel) tbl_usuarios.getModel();
                        tableModel.setRowCount(0);
                        MostrarUsuarios();
                        break;
                    }
                } else if (i == banco.bancoDados.length - 1) {
                    JOptionPane.showMessageDialog(null, "ID de usuário não encontrado");
                }
            }
        }

    }//GEN-LAST:event_btn_removerActionPerformed

    private void btn_alterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_alterarActionPerformed
        try {

            String resposta = JOptionPane.showInputDialog(null, "Digite o ID do usuário que você deseja fazer alterações");
            if (resposta != null) {
                TelaModeradorAlterarCL novatela = new TelaModeradorAlterarCL();

                novatela.banco = banco;
                novatela.volta = this;

                novatela.setID(Integer.parseInt(resposta));
                novatela.preencherCampos();
                novatela.setVisible(true);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Usuário não identificado! ");
        }
    }//GEN-LAST:event_btn_alterarActionPerformed

