    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       
        if (banco.moderadorCadastrar(this)) {
        volta.tableModel = (DefaultTableModel) volta.tbl_usuarios.getModel();
        volta.tableModel.setRowCount(0);
        volta.MostrarUsuarios();
        
        
        for (UsuarioCL bancoDado : banco.bancoDados) {
            if (bancoDado != null) {
                System.out.println("ID: " + bancoDado.getID());
                System.out.println("CPF: " + bancoDado.getCpf());
                System.out.println("Nome: " + bancoDado.getNome());
                System.out.println("Senha: " + bancoDado.getSenha());
                System.out.println("Email: " + bancoDado.getEmail());
                System.out.println("Nome de Usuario: " + bancoDado.getNomedeusuario());
                System.out.println("Telefone: " + bancoDado.getTelefone());
                System.out.println("Saldo: " + bancoDado.getSaldo());
                System.out.println();
            }
        }
        dispose();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

